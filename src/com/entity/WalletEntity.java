package com.entity;

public class WalletEntity {
	int WalletID;
	String WalletName;
	int WalletToUser;
	public int getWalletID() {
		return WalletID;
	}
	public void setWalletID(int walletID) {
		WalletID = walletID;
	}
	public String getWalletName() {
		return WalletName;
	}
	public void setWalletName(String walletName) {
		WalletName = walletName;
	}
	public int getWalletToUser() {
		return WalletToUser;
	}
	public void setWalletToUser(int walletToUser) {
		WalletToUser = walletToUser;
	}
	

}
